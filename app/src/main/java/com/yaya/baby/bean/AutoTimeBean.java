package com.yaya.baby.bean;

/**
 * @author : Darcy
 * @Date ${date}
 * @Description 自动选择时间的实体类
 */
public class AutoTimeBean {

    private String week;

    private String date;


    public String getWeek() {
        return week;
    }

    public void setWeek(String week) {
        this.week = week;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
}
